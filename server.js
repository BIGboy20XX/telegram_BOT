// üöÄ –ø—Ä–æ–≤–µ—Ä–∫–∞ —Å–∞–π—Ç–æ–≤ –∫–∞–∂–¥—ã–µ 60 —Å–µ–∫—É–Ω–¥
setInterval(async () => {
  const users = await pool.query("SELECT * FROM users WHERE monitoring=true");
  for (const user of users.rows) {
    const sites = await pool.query("SELECT * FROM sites WHERE chat_id=$1", [user.chat_id]);
    for (const site of sites.rows) {
      try {
        const res = await fetch(site.url);
        const text = await res.text();
        const hash = crypto.createHash("md5").update(text).digest("hex");

        if (site.last_hash && site.last_hash !== hash) {
          const now = new Date();
          const formatted = now.toLocaleString("ru-RU", { timeZone: "Asia/Almaty" });
          await sendTelegramMessage(
            user.chat_id,
            `‚ö° –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –Ω–∞ <b>${site.url}</b>\nüïí –í—Ä–µ–º—è: ${formatted}`
          );
          await pool.query(
            "UPDATE sites SET last_hash=$1, last_update=NOW() WHERE id=$2",
            [hash, site.id]
          );
        } else if (!site.last_hash) {
          await sendTelegramMessage(user.chat_id, `üîç –ù–∞—á–∞–ª –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥: <b>${site.url}</b>`);
          await pool.query(
            "UPDATE sites SET last_hash=$1, last_update=NOW() WHERE id=$2",
            [hash, site.id]
          );
        }
      } catch (err) {
        await sendTelegramMessage(user.chat_id, `‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ <b>${site.url}</b>: ${err.message}`);
      }
    }
  }
}, 60_000);

// üì© –æ—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏–π
async function sendTelegramMessage(chatId, text, extra = {}) {
  const safeText = text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");

  try {
    const res = await fetch(`${TELEGRAM_API}/sendMessage`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        chat_id: chatId,
        text: safeText,
        parse_mode: "HTML",
        disable_web_page_preview: true,
        ...extra
      })
    });

    const data = await res.json();
    console.log("–û—Ç–≤–µ—Ç Telegram:", data);
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è:", err);
  }
}

